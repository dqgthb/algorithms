def solve(n, d):
    if d == 1:
        return (2 ** n - 1) * 2

    if 2 * n - 1 == d:
        return 2

    if 2 * n - 1 > d:
        return 0

    c1 = solve(n-1, d-1) * 2
    c2 = 0
    for i in (1, d):
        c2 += solve(n-1, i) * solve(n-1, d-i)
    c3 = solve(n-1, d) * 2

    return c1 + c2 + c3






