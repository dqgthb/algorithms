def makeHeight(h):
    time = 0
    blocks = 0
    for i in range(N):
        for j in range(M):
            c = mat[i][j]
            if h > c:
                time += h - c
                blocks += h - c
            elif h < c:
                time += 2 * (c - h)
                blocks -= (c - h)
    if blocks > B:
        return -1
    return time


